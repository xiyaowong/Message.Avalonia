namespace Message.Avalonia.Models;

public enum MessagePosition
{
    /// <summary>
    ///     Places the message in the bottom right corner of the host.
    /// </summary>
    BottomRight,

    /// <summary>
    ///     Places the message in the bottom left corner of the host.
    /// </summary>
    BottomLeft,

    /// <summary>
    ///     Places the message in the bottom center corner of the host.
    /// </summary>
    BottomCenter,

    /// <summary>
    ///     Places the message in the top right corner of the host.
    /// </summary>
    TopRight,

    /// <summary>
    ///     Places the message in the top left corner of the host.
    /// </summary>
    TopLeft,

    /// <summary>
    ///     Places the message in the top center corner of the host.
    /// </summary>
    TopCenter,

    /// <summary>
    ///     Places the message in the center of the host.
    /// </summary>
    CenterCenter,
}
